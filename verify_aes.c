static int run_vector(const AESTestVector* tv) {
    AES_ctx ctx;
    uint8_t out[16];
    uint8_t back[16];

    if (AES_init(&ctx, tv->key, tv->key_len) != AES_OK) {
        printf("[FAIL] %s: AES_init failed\n", tv->name);
        return 1;
    }

    AES_encryptBlock(&ctx, tv->pt, out);
    if (memcmp(out, tv->ct, sizeof(out)) != 0) {
        printf("[FAIL] %s: encrypt mismatch\n", tv->name);
        return 1;
    }

    AES_decryptBlock(&ctx, tv->ct, back);
    if (memcmp(back, tv->pt, sizeof(back)) != 0) {
        printf("[FAIL] %s: decrypt mismatch\n", tv->name);
        return 1;
    }

    printf("[PASS] %s\n", tv->name);
    return 0;
}

int main(void) {
    int fail = 0;
    for (size_t i = 0; i < sizeof(vectors)/sizeof(vectors[0]); ++i) {
        fail |= run_vector(&vectors[i]);
    }
    if (fail) {
        printf("\nSummary: FAILURE\n");
        return 1;
    }
    printf("\nSummary: all vectors passed\n");
    return 0;
}

